#include "task.hpp"

//-----------------------------------------------------------------------------
Task::Task()
    : m_number{}
    , m_text{}
{}

//-----------------------------------------------------------------------------
int Task::getNumber()
{
    return m_number;
}

//-----------------------------------------------------------------------------
std::string Task::getText()
{
    return m_text;
}

//-----------------------------------------------------------------------------
void Task::setText(const std::string &text)
{
    m_text = text;
}

//-----------------------------------------------------------------------------
void Task::setNumber(int number)
{
    m_number = number;
}
